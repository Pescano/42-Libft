#include "libft.h"

int	ft_atoi(const char *str)
{
	int	x[3];

	x[0] = 0;
	x[1] = 1;
	x[2] = 0;
	while ((str[x[0]] >= '\t' && str[x[0]] <= '\r') || str[x[0]] == ' ')
		x[0]++;
	if (str[x[0]] == '-' || str[x[0]] == '+')
	{
		if (str[x[0]] == '-')
			x[1] = -1;
		x[0]++;
	}
	while (str[x[0]] >= '0' && str[x[0]] <= '9')
	{
		x[2] = x[2] * 10;
		x[2] = x[2] + str[x[0]] - 48;
		x[0]++;
	}
	x[2] = x[2] * x[1];
	return (x[2]);
}
